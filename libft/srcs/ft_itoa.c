#include "libft.h"

char		*ft_itoa(int n)
{
	char	*ret;
	int		i;
	int		neg;

	neg = (n < 0 ? 1 : 0);
	i = ftb_intlen(n);
	if ((ret = malloc(sizeof(char) * (i + 1))) == 0)
		return (NULL);
	ret[i--] = '\0';
	if (n < 0)
	{
		if (n == -2147483648)
			ret[i--] = '8';
		n = (n == -2147483648 ? 214748364 : -n);
	}
	while (i >= 0)
	{
		ret[i--] = (n % 10) + 48;
		n /= 10;
	}
	if (neg == 1)
		ret[0] = '-';
	return (ret);
}
