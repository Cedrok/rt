#include "libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (!s1 || !s2)
		return (0);
	while (n > 0 && *s1 != '\0')
	{
		if (*s1 != *s2)
			return (0);
		s1++;
		s2++;
		n--;
	}
	if (n == 0)
		return (1);
	else if (*s2 != *s1)
		return (0);
	return (1);
}
